// This is Normal Loop File

public class Main {
    public static void main(String[] args) {
        int i = 0;
        for(; i<=10; i=i+2){
            System.out.println("This will print all even Number from 0 to 10 : "+i);
        }
        System.out.println("This is Testing for Loop in Java: ");
        System.out.println("This Will print i value End of the loop : " +i);
    }
}